package com.cunhaedu.student_database.domain;

import com.cunhaedu.student_database.domain.enums.GenderEnum;

import java.time.LocalDate;

public class Person {
    private int id;
    private String name;
    private String cpf;
    private String photoLink;
    private LocalDate birthDate;
    private GenderEnum gender;
    private String address;
    private FederativeUnit federativeUnit;
    private String city;
    private String email;
    private String cellphone;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getPhotoLink() {
        return photoLink;
    }

    public void setPhotoLink(String photoLink) {
        this.photoLink = photoLink;
    }

    public LocalDate getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(LocalDate birthDate) {
        this.birthDate = birthDate;
    }

    public GenderEnum getGender() {
        return gender;
    }

    public void setGender(GenderEnum gender) {
        this.gender = gender;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public FederativeUnit getFederativeUnit() {
        return federativeUnit;
    }

    public void setFederativeUnit(FederativeUnit federativeUnit) {
        this.federativeUnit = federativeUnit;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCellphone() {
        return cellphone;
    }

    public void setCellphone(String cellphone) {
        this.cellphone = cellphone;
    }
}
